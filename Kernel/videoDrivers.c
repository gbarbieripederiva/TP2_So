{
    uint8_t *current = (uint8_t *)((uint64_t)VBEModeInfoBlock->framebuffer); //another warning to hide
    uint8_t *shouldBe;
    int i = 0;
    while (i < VBEModeInfoBlock->height - 2 * PIXELS_PER_CHAR)
    {
        shouldBe = current + 2 * PIXELS_PER_CHAR * VBEModeInfoBlock->pitch;
        for (int j = 0; j < VBEModeInfoBlock->width; j++)
        {
            drawPixel(current + j * pixelWidth, shouldBe[0], shouldBe[1], shouldBe[2]);
            shouldBe += pixelWidth;
        }
        current += VBEModeInfoBlock->pitch;
        i++;
    }
    drawRectangle(coordinate(0, VBEModeInfoBlock->height - 2 * PIXELS_PER_CHAR), VBEModeInfoBlock->width, 2 * PIXELS_PER_CHAR, V_BLACK); //paints last two lines in black after moving everything up
}

void writeChar(uint8_t character)
{
    if (column >= VBEModeInfoBlock->width / PIXELS_PER_CHAR)
    {
        if (line == VBEModeInfoBlock->height / PIXELS_PER_CHAR - 2)
        {
            moveScreenUp();
        }
        else
        {
            line += 2;
        }
        column = 0;
    }
    drawCharConsole(coordinate(column * PIXELS_PER_CHAR, line * PIXELS_PER_CHAR), character);
    column++;
}

void writeString(const uint8_t *string)
{
    while (string[0])
    {
        writeChar(string[0]);
        string++;
    }
}

void newLine()
{
    if (line == VBEModeInfoBlock->height / PIXELS_PER_CHAR - 2)
    {
        moveScreenUp();
    }
    else
    {
        line += 2;
    }
    column = 0;
}

void backSpace()
{
    if (!column)
    {
        if (line)
        {
            line -= 2;
            column = VBEModeInfoBlock->width / PIXELS_PER_CHAR;
        }
    }
    column--;
    drawSquare(coordinate(column * PIXELS_PER_CHAR, line * PIXELS_PER_CHAR), 8, V_BLACK);
}

void getScreenSize(uint16_t *width, uint16_t *height)
{
    *width = VBEModeInfoBlock->width;
    *height = VBEModeInfoBlock->height;
}
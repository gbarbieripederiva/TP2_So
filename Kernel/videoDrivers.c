const modeInfo *VBEModeInfoBlock = (modeInfo *)0x0000000000005C00;

uint8_t pixelWidth;

void initializeDriver()
{
    pixelWidth = VBEModeInfoBlock->bpp / 8;
}

uint8_t *coordinate(uint16_t x, uint16_t y)
{
    return (uint8_t *)((uint64_t)VBEModeInfoBlock->framebuffer + x * pixelWidth + y * VBEModeInfoBlock->pitch); //how do I hide this warning?
}

colour toColour(uint8_t blue, uint8_t green, uint8_t red)
{
    return (blue << 16) | (green << 8) | red;
}

static uint8_t retrieveBlue(colour c)
{
    return c >> 16;
}

static uint8_t retrieveGreen(colour c)
{
    return (c >> 8); //& 0xFF;                cast to uint8 just takes 8 least significant bits
}

static uint8_t retrieveRed(colour c)
{
    return c; //& 0xFF;                       same as above
}

void drawPixel(uint8_t *coordinate, uint8_t blue, uint8_t green, uint8_t red)
{
    coordinate[0] = blue;
    coordinate[1] = green;
    coordinate[2] = red;
}

static void drawRectangleNaked(uint8_t *coordinate, uint16_t base, uint16_t height, uint8_t blue, uint8_t green, uint8_t red)
{
    for (int i = 0; i < height; i++)
    {
        for (int j = 0; j < base; j++)
        {
            coordinate[j * pixelWidth] = blue;
            coordinate[j * pixelWidth + 1] = green;
            coordinate[j * pixelWidth + 2] = red;
        }
        coordinate += VBEModeInfoBlock->pitch;
    }
}

void drawRectangle(uint8_t *coordinate, uint16_t base, uint16_t height, colour c)
{
    drawRectangleNaked(coordinate, base, height, retrieveBlue(c), retrieveGreen(c), retrieveRed(c));
}

static void drawSquareNaked(uint8_t *coordinate, uint16_t side, uint8_t blue, uint8_t green, uint8_t red)
{
    drawRectangleNaked(coordinate, side, side, blue, green, red);
}

void drawSquare(uint8_t *coordinate, uint16_t side, colour c)
{
    drawRectangle(coordinate, side, side, c);
}

static void drawCharNaked(uint8_t *coordinate, uint8_t character, uint8_t fontSize, uint8_t foregroundBlue,
                          uint8_t foregroundGreen, uint8_t foregroundRed, uint8_t backgroundBlue, uint8_t backgroundGreen, uint8_t backgroundRed)
{
    uint8_t *rows = characters[character];
    uint8_t row;
    for (int i = 0; i < 8; i++)
    {
        row = rows[i];
        for (int j = 7; j >= 0; j--)
        {
            if (row & (1 << j))
            {
                drawSquareNaked(coordinate + j * pixelWidth * fontSize, fontSize, foregroundBlue, foregroundGreen, foregroundRed);
            }
            else
            {
                drawSquareNaked(coordinate + j * pixelWidth * fontSize, fontSize, backgroundBlue, backgroundGreen, backgroundRed);
            }
        }
        coordinate += fontSize * VBEModeInfoBlock->pitch;
    }
}

void drawCharSized(uint8_t *coordinate, uint8_t character, uint8_t fontSize, colour foreground, colour background)
{
    drawCharNaked(coordinate, character, fontSize, retrieveBlue(foreground), retrieveGreen(foreground), retrieveRed(foreground),
                  retrieveBlue(background), retrieveGreen(background), retrieveRed(background));
}

void drawChar(uint8_t *coordinate, uint8_t character)
{
    drawCharSized(coordinate, character, 1, V_WHITE, V_BLACK);
}

void drawCharAt(uint16_t x, uint16_t y, uint8_t character){
    drawChar(coordinate(x,y), character);
}

void drawStringSized(uint8_t *coordinate, uint8_t *string, uint8_t fontSize, colour foreground, colour background)
{
    uint8_t foregroundBlue = retrieveBlue(foreground);
    uint8_t foregroundGreen = retrieveGreen(foreground);
    uint8_t foregroundRed = retrieveRed(foreground);
    uint8_t backgroundBlue = retrieveBlue(background);
    uint8_t backgroundGreen = retrieveGreen(background);
    uint8_t backgroundRed = retrieveRed(background);
    while (string[0])
    {
        drawCharNaked(coordinate, string[0], fontSize, foregroundBlue, foregroundGreen, foregroundRed,
                      backgroundBlue, backgroundGreen, backgroundRed);
        coordinate += 8 * fontSize * pixelWidth;
        string++;
    }
}

void drawString(uint8_t *coordinate, uint8_t *string)
{
    drawStringSized(coordinate, string, 1, V_WHITE, V_BLACK);
}

void drawCharConsole(uint8_t *coordinate, uint8_t character)
{ //slightly optimized for writing white over black
    uint8_t *rows = characters[character];
    uint8_t row;
    for (int i = 0; i < 8; i++)
    {
        row = rows[i];
        for (int j = 7; j >= 0; j--)
        {
            if (row & (1 << j))
            {
                drawSquareNaked(coordinate + j * pixelWidth, 1, 255, 255, 255);
            }
        }
        coordinate += VBEModeInfoBlock->pitch;
    }
}


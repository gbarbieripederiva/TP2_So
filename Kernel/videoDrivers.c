uint16_t line;
uint16_t column;

void setCursor(uint16_t x, uint16_t y){
    line = y;
    column = x;
}

void initializeConsole()
{
    initializeDriver();
    line = 0;
    column = 0;
}

void clear()
{
    drawRectangle(coordinate(0, 0), VBEModeInfoBlock->width, VBEModeInfoBlock->height, V_BLACK);
    line = 0;
    column = 0;
}

